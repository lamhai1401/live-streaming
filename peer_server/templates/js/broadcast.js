function convert_str_to_list(sdp_string, character) {
    return sdp_string.split(character)
}

function convert_list_to_string(sdp_list, character) {
    return sdp_list.join(character)
}

function sdp_filter(sdp_raw, engine) {
    /**
    sdp_raw : {
        a=rtpmap:96 VP8/90000
        a=rtcp-fb:96 goog-remb
        a=rtcp-fb:96 transport-cc
        a=rtcp-fb:96 ccm fir
        a=rtcp-fb:96 nack
        a=rtcp-fb:96 nack pli
        }
    engine : VP8/H.264/....
     * 
     */

    sdp_temp = []

    sdp_list = convert_str_to_list(sdp_raw, "\r\n")
    engine_number = null

    rtp_map = "a=rtpmap:"
    
    rtcp_fb = "a=rtcp-fb:"
    rtcp_fb_vp8 = null

    fmtp = "a=fmtp:"
    fmtp_vp8 = null

    media_video = "m=video"
    is_video_format = false
    is_first_rtx = true

    //  for line in sdp_raw
    for (i = 0; i < sdp_list.length; i++) {
        if (sdp_list[i].startsWith("m=")) {
            if (sdp_list[i].startsWith(media_video)) {
                is_video_format = true
                sdp_temp.push(sdp_list[i])
            } 
            else {
                is_video_format = false
                sdp_temp.push(sdp_list[i])
            }
        }
        else if (sdp_list[i].startsWith(rtp_map)) {
            if (!is_video_format){
                sdp_temp.push(sdp_list[i])
            }
            else if (sdp_list[i].includes(engine)) {
                var numberPattern = /\d+/g;
                engine_number = sdp_list[i].match(numberPattern)[0]
                sdp_temp.push(sdp_list[i])

                rtcp_fb_vp8 = rtcp_fb + engine_number
                fmtp_vp8 = fmtp_vp8 + engine_number
            }
            else if (sdp_list[i].includes("rtx") && engine_number != null && is_first_rtx) {
                sdp_temp.push(sdp_list[i])
                is_first_rtx = false
            }
            else {
                continue
            }
        }
        else if (sdp_list[i].startsWith(rtcp_fb)) {
            if (rtcp_fb_vp8 && sdp_list[i].startsWith(rtcp_fb_vp8)) {
                sdp_temp.push(sdp_list[i])
            }
            else {
                continue
            }
        }
        else if (sdp_list[i].startsWith(fmtp)){
            if (fmtp_vp8 && sdp_list[i].startsWith(fmtp_vp8)) {
                sdp_temp.push(sdp_list[i])
            }
            else if (engine_number && sdp_list[i].includes(engine_number)) {
                sdp_temp.push(sdp_list[i])
            }
            else {
                continue
            }
        }
        else {
            sdp_temp.push(sdp_list[i])
        }
    }
    return convert_list_to_string(sdp_temp, "\r\n")
}

function negotiate() {
    return pc.createOffer().then(function(offer) {
        // offer.sdp = sdp_filter(offer.sdp, "VP8")
        return pc.setLocalDescription(offer);
    }).then(function() {
        // wait for ICE gathering to complete
        return new Promise(function(resolve) {
            if (pc.iceGatheringState === 'complete') {
                resolve();
            } else {
                function checkState() {
                    if (pc.iceGatheringState === 'complete') {
                        pc.removeEventListener('icegatheringstatechange', checkState);
                        resolve();
                    }
                }
                pc.addEventListener('icegatheringstatechange', checkState);
            }
        });
    }).then(function() {
        var offer = pc.localDescription;
        var codec;

        codec = document.getElementById('audio-codec').value;
        if (codec !== 'default') {
            offer.sdp = sdpFilterCodec('audio', codec, offer.sdp);
        }

        codec = document.getElementById('video-codec').value;
        if (codec !== 'default') {
            offer.sdp = sdpFilterCodec('video', codec, offer.sdp);
        }

        roomId = document.getElementById("idRoom").value
        document.getElementById('offer-sdp').textContent = offer.sdp;
        return fetch('/broadcast', {
            body: JSON.stringify({
                idRoom: roomId,
                session: {
                    "sdp": offer.sdp,
                    "type": offer.type,
                },
                video_transform: document.getElementById('video-transform').value
            }),
            headers: {
                'Content-Type': 'application/json'
            },
            method: 'POST'
        });
    }).then(function(response) {
        return response.json();
    }).then(function(resp) {
        console.log("resp: ", resp)
        answer = resp['answer']
        // answer.sdp = sdp_filter(answer.SDP, "VP8")
        document.getElementById('answer-sdp').textContent = answer.SDP;
        return pc.setRemoteDescription({
            sdp: answer.sdp,
            type: answer.type
        });
    }).catch(function(e) {
        alert(e);
    });
}

function start() {
    document.getElementById('start').style.display = 'none';

    pc = createPeerConnection();

    var time_start = null;

    function current_stamp() {
        if (time_start === null) {
            time_start = new Date().getTime();
            return 0;
        } else {
            return new Date().getTime() - time_start;
        }
    }

    if (document.getElementById('use-datachannel').checked) {
        var parameters = JSON.parse(document.getElementById('datachannel-parameters').value);

        dc = pc.createDataChannel('chat', parameters);
        dc.onclose = function() {
            clearInterval(dcInterval);
            dataChannelLog.textContent += '- close\n';
        };
        dc.onopen = function() {
            dataChannelLog.textContent += '- open\n';
            dcInterval = setInterval(function() {
                var message = 'ping ' + current_stamp();
                dataChannelLog.textContent += '> ' + message + '\n';
                dc.send(message);
            }, 1000);
        };
        dc.onmessage = function(evt) {
            dataChannelLog.textContent += '< ' + evt.data + '\n';

            if (evt.data.substring(0, 4) === 'pong') {
                var elapsed_ms = current_stamp() - parseInt(evt.data.substring(5), 10);
                dataChannelLog.textContent += ' RTT ' + elapsed_ms + ' ms\n';
            }
        };
    }

    var constraints = {
        audio: document.getElementById('use-audio').checked,
        video: false
    };

    if (document.getElementById('use-video').checked) {
        var resolution = document.getElementById('video-resolution').value;
        if (resolution) {
            resolution = resolution.split('x');
            constraints.video = {
                width: parseInt(resolution[0], 0),
                height: parseInt(resolution[1], 0)
            };
        } else {
            constraints.video = true;
        }
    }

    if (constraints.audio || constraints.video) {
        if (constraints.video) {
            document.getElementById('media').style.display = 'block';
        }
        navigator.mediaDevices.getUserMedia(constraints).then(function(stream) {
            stream.getTracks().forEach(function(track) {
                pc.addTrack(track, stream);
            });
            return negotiate();
        }, function(err) {
            alert('Could not acquire media: ' + err);
        });
    } else {
        negotiate();
    }

    document.getElementById('stop').style.display = 'inline-block';
}

function stop() {
    document.getElementById('stop').style.display = 'none';

    // close data channel
    if (dc) {
        dc.close();
    }

    // close transceivers
    if (pc.getTransceivers) {
        pc.getTransceivers().forEach(function(transceiver) {
            if (transceiver.stop) {
                transceiver.stop();
            }
        });
    }

    // close local audio / video
    pc.getSenders().forEach(function(sender) {
        sender.track.stop();
    });

    // close peer connection
    setTimeout(function() {
        pc.close();
    }, 500);
}

function sdpFilterCodec(kind, codec, realSdp) {
    var allowed = []
    var rtxRegex = new RegExp('a=fmtp:(\\d+) apt=(\\d+)\r$');
    var codecRegex = new RegExp('a=rtpmap:([0-9]+) ' + escapeRegExp(codec))
    var videoRegex = new RegExp('(m=' + kind + ' .*?)( ([0-9]+))*\\s*$')
    
    var lines = realSdp.split('\n');

    var isKind = false;
    for (var i = 0; i < lines.length; i++) {
        if (lines[i].startsWith('m=' + kind + ' ')) {
            isKind = true;
        } else if (lines[i].startsWith('m=')) {
            isKind = false;
        }

        if (isKind) {
            var match = lines[i].match(codecRegex);
            if (match) {
                allowed.push(parseInt(match[1]));
            }

            match = lines[i].match(rtxRegex);
            if (match && allowed.includes(parseInt(match[2]))) {
                allowed.push(parseInt(match[1]));
            }
        }
    }

    var skipRegex = 'a=(fmtp|rtcp-fb|rtpmap):([0-9]+)';
    var sdp = '';

    isKind = false;
    for (var i = 0; i < lines.length; i++) {
        if (lines[i].startsWith('m=' + kind + ' ')) {
            isKind = true;
        } else if (lines[i].startsWith('m=')) {
            isKind = false;
        }

        if (isKind) {
            var skipMatch = lines[i].match(skipRegex);
            if (skipMatch && !allowed.includes(parseInt(skipMatch[2]))) {
                continue;
            } else if (lines[i].match(videoRegex)) {
                sdp += lines[i].replace(videoRegex, '$1 ' + allowed.join(' ')) + '\n';
            } else {
                sdp += lines[i] + '\n';
            }
        } else {
            sdp += lines[i] + '\n';
        }
    }

    return sdp;
}

function escapeRegExp(string) {
    return string.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'); // $& means the whole matched string
}
